    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        boolean resultado = false;
        int codigo=Integer.parseInt(txtCodigo.getText());
        String color= txtColor.getText();
        String marca = txtMarca.getText();
        String modelo= txtModelo.getText();
        String placa = txtPlaca.getText();
        
        int codigo2=Integer.parseInt(txtCod.getText());
        String cedula= txtCedula.getText();
        String nombre= txtNombre.getText();
        String Apellido=txtApellido.getText();
        String direccion = txtDireccion.getText();
        
        Vehiculo v= new Vehiculo(placa);
        
        if (color.isEmpty()||marca.isEmpty()||modelo.isEmpty()||placa.isEmpty()||txtCod.getText().equals("") ||cedula.isEmpty() || nombre.isEmpty() || Apellido.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Llene todos los campos solicitados");
        }else{
            if(controladorVehiculo.read(v)!= null){
                Cliente cliente = new Cliente(codigo2, cedula, nombre, Apellido, direccion);
                Vehiculo vehiculo= new Vehiculo(codigo, color, marca, modelo, placa, cliente);
                resultado =controladorVehiculo.update(vehiculo);    
            }else{
               Cliente cliente = new Cliente(codigo2, cedula, nombre, Apellido, direccion);
               Vehiculo vehiculo= new Vehiculo(codigo, color, marca, modelo, placa, cliente);
               resultado =controladorVehiculo.create(vehiculo);  
            }  
            limpiar();
            JOptionPane.showMessageDialog(this, "Operaci√≥n : " + resultado);
            cargarDatos();
            dispose();
        }
    }//GEN-LAST:event_btnCrearActionPerformed

    private void tblVehiculosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblVehiculosMouseClicked
        int fila = tblVehiculos.getSelectedRow();
        int cod = (int) tblVehiculos.getValueAt(fila, 0);
        String color = (String)tblVehiculos.getValueAt(fila, 1);
        String Marca = (String) tblVehiculos.getValueAt(fila, 2);
        String modelo = (String) tblVehiculos.getValueAt(fila, 3);
        String placa = (String) tblVehiculos.getValueAt(fila, 4);
        Cliente cliente =(Cliente) tblVehiculos.getValueAt(fila, 5);
        
        int cod2= cliente.getCodigo();
        String cedula= cliente.getCedula();
        String nombre= cliente.getNombre();
        String Apellido = cliente.getApellido();
        String direccion= cliente.getDireccion();

        txtCodigo.setText(String.valueOf(cod));
        txtColor.setText(color);
        txtMarca.setText(Marca);
        txtModelo.setText(modelo);
        txtPlaca.setText(placa);
        txtCod.setText(String.valueOf(cod2));
        txtCedula.setText(cedula);
        txtNombre.setText(nombre);
        txtApellido.setText(Apellido);
        txtDireccion.setText(direccion);
    }//GEN-LAST:event_tblVehiculosMouseClicked


