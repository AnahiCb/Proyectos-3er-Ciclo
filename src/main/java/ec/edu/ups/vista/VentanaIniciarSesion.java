    private void btnIniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarActionPerformed
        Usuario usuarioEncontrado = controladorUsuario.iniciarSesion(txtEmail.getText(), String.valueOf(txtContraseña.getPassword()));
        System.out.println(usuarioEncontrado);
        if (usuarioEncontrado != null) {
            JOptionPane.showMessageDialog(this, "Usted ha iniciado sesión");
            gestionTelefono.setUsuario(usuarioEncontrado);
            ventanaPrincipal.getMenuGestion().setEnabled(true);
            ventanaPrincipal.getCerrarSesion().setEnabled(true);
            ventanaPrincipal.getRegistroUsuario().setEnabled(false);
            ventanaPrincipal.getInicioSesion().setEnabled(false);
            limpiar();
            this.hide();

        } else {
            JOptionPane.showMessageDialog(this, "Usuario no encontrado, regístrese");
        }
    }//GEN-LAST:event_btnIniciarActionPerformed


